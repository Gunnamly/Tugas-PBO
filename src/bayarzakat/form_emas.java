    private void EnamaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_EnamaKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        
        if(!(Character.isAlphabetic(c) || (c==KeyEvent.VK_SPACE) || (c==KeyEvent.VK_DELETE) || (c==KeyEvent.VK_PERIOD))){       
            evt.consume();
        }
    }//GEN-LAST:event_EnamaKeyTyped

