    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (Enominal.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nominal tidak boleh kosong");
           
        }else if (Enama.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong");
        
        }else if (Ewhatsapp.getText().equals("")){
            JOptionPane.showMessageDialog(null, "No Whatsapp tidak boleh kosong");
            
        }else if (Eemail.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Email tidak boleh kosong");
            
        }else if (Edoa.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Doa tidak boleh kosong");
            
        }else{
            String Bank = Combo_bank.getSelectedItem().toString();
            int nml = Integer.parseInt(Enominal.getText());
            if (Bank.equals("-")){
                JOptionPane.showMessageDialog(null, "Pilihan Bank Tidak Boleh Kosong");
            if (nml <= 125000){
                JOptionPane.showMessageDialog(null, "Minimal Donasi Rp125.000");
                Enominal.setText("");
            }
            }else{
                String sql = "insert into emas values (?,?,?,?,?,?)";
                    try{
                        PreparedStatement stat = conn.prepareStatement(sql);
                        stat.setInt(1, nml);
                        stat.setString(2, Enama.getText());
                        stat.setString(3, Ewhatsapp.getText());
                        stat.setString(4, Eemail.getText());
                        stat.setString(5, Edoa.getText());
                        stat.setString(6, Bank);
                        stat.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Donasi Berhasil !");
                    }catch (SQLException e) {
                        JOptionPane.showMessageDialog(null, "Donasi Gagal !" +e);
                    }  
                
            }
            
        }    
    }//GEN-LAST:event_jButton1ActionPerformed

